// C1: Khai báo biến bằng "var"
var a = 3
var b = 5
var c = a + b
var d = a * b
console.log("a = " + a)
console.log("b = " + b)
console.log("c = a + b" + c)
console.log("d = a * b" + d)

// C2: Khai báo biến số(có thể thay đổi được) bằng "let"
let city = "Hanoi"
let country = "Vietnam"
console.log(city + " is the capital of " + country)

//C3:Khai báo hằng số(không thể thay đổi được) bằng "const"
const pi = 3.14
console.log("Pi = " + pi)